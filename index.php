                Atendiendo a la demanda del sector de software en cuanto a la necesidad de incorporar recurso humano calificado, se propone continuar generando capacitaciones que permitan que salteños y salteñas se formen en programación. Estas propuestas no sólo permiten cubrir las vacantes solicitadas por las empresas, sino que también generan
                oportunidades de inserción laboral entre los jóvenes.
                Así desde septiembre de 2021, se comenzó a implementar el proyecto Mil Programadores para dar una respuesta puntual al sector. Con la intención de continuar formando futuros talentos se proponen la realización de programas de capacitación como Programadoras, Tecnicatura Universitaria en Desarrollo de Software, impartida por la Universidad Provincial de Administración, Tecnológica y Oficio (UPATeCO) de la provincia, y cursos de Introducción a la programación junto al Ministerio de Trabajo, Empleo y Seguridad Social de la Nación.
                Mil Programadores
                El Mil Programadores se centra en la capacitación de salteños y salteñas en programación empleando los lenguajes PYTHON y JAVA como respuesta a la demanda puntual del sector de las Tecnologías de la Información y la Comunicación (TICs).
                Este proyecto se focaliza en la inserción en el mercado laboral de los perfiles capacitados y en la generación de un ecosistema de innovación y tecnología en Salta como visión de espacio generador de igualdad de oportunidades y desarrollo.
            </p>
            <p class="text">
                <br><span>Objetivo General:</span> <br> <br> 
                Fomentar la economía del conocimiento como motor de desarrollo económico en la provincia de Salta, capacitando recursos humanos en el área de las TICs para dar respuesta a una necesidad puntual del sector. <br><br>
                <span>Objetivos Específicos:</span>  <br> <br> 
                Formar y fomentar recursos humanos calificados en programación. <br>
                Formar a jóvenes profesionales en habilidades blandas para el futuro desempeño laboral, orientado a la programación. <br>
                Fomentar el sector de la economía del conocimiento y la participación de empresas del sector para la incorporación de los perfiles formados por el programa. <br>
                Crear una red provincial de programadores para el fomento de un ecosistema tecnológico local con impacto nacional y global. <br>

            </p>
            <p class="text">
                <br> El proyecto está destinado a jóvenes y adultos a partir de los 18 pertenecientes a la Provincia de Salta, con interés en desarrollar habilidades de programación o bien, con interés en insertarse en el mercado local, regional e internacional en el ámbito de SSI <br>
                Actualmente este proyecto se encuentra en su tercera edición. La primera edición dio inicio el 30 de septiembre de 2021 con el dictado del curso “Introducción a la programación con Python”, el cual tuvo una alta respuesta por parte de los estudiantes con más de 7.000 preinscripciones. <br> 
                Durante el primer cuatrimestre del 2022, se llevó a cabo la segunda edición de Mil Programadores implementando el lenguaje de programación Java.
